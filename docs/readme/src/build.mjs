import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { badges, meta, navLinks, shieldUrl } from './config.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const repoRoot = path.resolve(root, '../..');
const sectionsDir = path.join(root, 'sections');

async function readSection(name) {
  return fs.readFile(path.join(sectionsDir, `${name}.md`), 'utf8');
}

function hero() {
  const badgeRow = badges.map((b) => `[![${b.label}](${shieldUrl(b)})](${meta.repo})`).join('\n');

  const nav = navLinks
    .map((l) => `[${l.label}](${l.href})`)
    .join(' · ');

  return `<!-- Generated by docs/readme — run: npm run build --prefix docs/readme -->
<div align="center">

<picture>
  <source media="(prefers-color-scheme: dark)" srcset="assets/logo.png">
  <img src="assets/logo.png" alt="${meta.name} logo" width="140" />
</picture>

# ${meta.name}

${badgeRow}

<br/>

<h3>${meta.tagline}</h3>

<p>
  <a href="${meta.repo}"><strong>GitHub</strong></a> ·
  <a href="#quick-start"><strong>Quick Start</strong></a> ·
  <a href="mcp_features.md"><strong>Feature Catalog</strong></a> ·
  <a href=".planning/ROADMAP.md"><strong>Roadmap</strong></a>
</p>

<p><sub>${nav}</sub></p>

<img src="assets/hero-banner.png" alt="${meta.name} — ${meta.tagline}" width="100%" />

</div>`;
}

function toc() {
  return `---

## Navigation

| | |
|:--|:--|
| **Repository** | [${meta.repo.replace('https://github.com/', '')}](${meta.repo}) |
| **npm package** | \`${meta.npm}\` — *coming soon* |
| **Coolify API** | ${meta.coolifyApi} (self-hosted) |
| **Status** | Planning — v1 in active design |
| **Languages** | [Deutsch](#deutsch) · [English](#english) |

<details>
<summary><strong>Table of contents</strong></summary>

- [At a glance](#at-a-glance)
- [Why Coolify MCP?](#why-coolify-mcp)
- [Quick Start](#quick-start)
- [Features](#features)
- [Architecture](#architecture)
- [Tool schema](#tool-schema)
- [Multi-instance](#multi-instance)
- [Security](#security)
- [Roadmap](#roadmap)
- [Development status](#development-status)
- [Contributing](#contributing)
- [Deutsch](#deutsch)
- [English](#english)

</details>`;
}

async function main() {
  const parts = [
    hero(),
    toc(),
    await readSection('at-a-glance'),
    await readSection('why'),
    await readSection('quick-start'),
    await readSection('features'),
    await readSection('architecture'),
    await readSection('tool-schema'),
    await readSection('multi-instance'),
    await readSection('security'),
    await readSection('roadmap'),
    await readSection('status'),
    await readSection('contributing'),
    await readSection('deutsch'),
    await readSection('english'),
    await readSection('footer'),
  ];

  const output = `${parts.join('\n\n')}\n`;
  const outPath = path.join(repoRoot, 'README.md');
  await fs.writeFile(outPath, output, 'utf8');
  console.log(`Wrote ${outPath} (${output.length} chars)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
